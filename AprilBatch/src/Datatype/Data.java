package Datatype;

public class Data {

	public static void main(String[] args) {
		
//		Premitive Datatype
//		(memory size fixed,called keywords,start with lower case )

		byte a = 1;                         // 1byte
		short b = 12;                      // 2byte
		int c = 1234;                     // 4byte
		long d = 12345678l;              // 8 byte denote by "l"

		float e = 25.25f;              // 4byte denote by f
		double f = 123456.08d;        // 8 byte denote by d

		char g = 'A';                // 2byte denote by ' '

		boolean h = true;          // 1bit, (1byte=8bit)
//  .......................................................................		
//		non premitive Datatype 
//		(memory size not fixed,called identifier,start with capital letter)
		
	String name = "amin";        // denote by " "
	// class name
	
//	output 
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(name);

	}

}
